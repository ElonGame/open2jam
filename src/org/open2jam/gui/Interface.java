    private class FileItem {
        File file;
        
        public FileItem(File f) {
            this.file = f;
        }

        @Override
        public int hashCode() {
            int hash = 7;
            hash = 59 * hash + (this.file != null ? this.file.hashCode() : 0);
            return hash;
        }
        
        @Override
        public boolean equals(Object o) {
            return (o instanceof FileItem) && 
                    ((FileItem)o).file.equals(file);
        }
        
        @Override
        public String toString() {
            return file.getName();
        }
    }

